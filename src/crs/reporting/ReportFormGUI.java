    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        // TODO add your handling code here:
        String studentId = txtStudentId.getText().trim();

        if (studentId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Student ID.");
            return;
        }

        StudentReport report = reportService.generateReport(studentId);

        if (report == null) {
            JOptionPane.showMessageDialog(this, "Student ID not found.");
            clearFields();
            return;
        }

        // Fill student info fields
        txtName.setText(report.getStudentName());
        txtMajor.setText(report.getMajor());
        txtYear.setText(report.getYear());

        // Fill table
        DefaultTableModel model = (DefaultTableModel) tableGrades.getModel();
        model.setRowCount(0); // clear old data

        report.getEntries().forEach(c -> {
            model.addRow(new Object[]{
                c.getCourseName(),
                c.getCredits(),
                c.getGrade()
            });
        });
    }//GEN-LAST:event_btnGenerateActionPerformed

    private void btnExportPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportPDFActionPerformed
        // TODO add your handling code here:
        String studentId = txtStudentId.getText().trim();

        if (studentId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Student ID first.");
            return;
        }

        StudentReport report = reportService.generateReport(studentId);

        if (report == null) {
            JOptionPane.showMessageDialog(this, "Please generate a valid report first.");
            return;
        }

        // Export Report
        pdfFileName = studentId + "_Report.pdf";
        PdfExporter exporter = new PdfExporter();
        exporter.exportReport(report, pdfFileName);

        // OPEN THE PDF AUTOMATICALLY
        try {
            File pdf = new File(pdfFileName);
            if (pdf.exists()) {
                Desktop.getDesktop().open(pdf);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnExportPDFActionPerformed

