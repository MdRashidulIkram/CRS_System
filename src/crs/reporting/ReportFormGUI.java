    private void addStudentIdListener() {
        txtStudentId.getDocument().addDocumentListener(new javax.swing.event.DocumentListener() {

            private void updateState() {
                String text = txtStudentId.getText().trim();
                boolean isValid = isValidStudentId(text);

                btnGenerate.setEnabled(isValid);
                btnExportPDF.setEnabled(isValid);
            }

            @Override
            public void insertUpdate(javax.swing.event.DocumentEvent e) {
                updateState();
            }

            @Override
            public void removeUpdate(javax.swing.event.DocumentEvent e) {
                updateState();
            }

            @Override
            public void changedUpdate(javax.swing.event.DocumentEvent e) {
                updateState();
            }
        });
    }

    private boolean isValidStudentId(String id) {
        return id.matches("S\\d{3}");
    }

    private void applyStudentIdMask(java.awt.event.KeyEvent e) {
        String text = txtStudentId.getText();

        // Character the user typed
        char ch = e.getKeyChar();

        // Only allow backspace/delete always
        if (ch == '\b' || ch == 127) {
            return;
        }

        // Length check (max 4 chars)
        if (text.length() >= 4) {
            e.consume();
            return;
        }

        // First character must be 'S'
        if (text.length() == 0) {
            // auto convert to uppercase S
            if (ch == 's' || ch == 'S') {
                txtStudentId.setText("S");
            } else {
                e.consume();
            }
            e.consume();
            return;
        }

        // Remaining characters must be digits
        if (!Character.isDigit(ch)) {
            e.consume();
        }
    }

 
