private void loadHistoryTable() {
    String userId = currentUser.getId();
    String filePath = "login_history.txt";

    String[] columns = {"Record ID", "Action", "Time (Readable)", "Time (Binary)"};
    DefaultTableModel model = new DefaultTableModel(columns, 0);

    try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
        String line;

        while ((line = br.readLine()) != null) {
            String[] p = line.split("\\|");

            if (p.length == 5) {
                String recordId = p[0];
                String fileUserId = p[1];
                String action = p[2];
                long timeMillis = Long.parseLong(p[3]);
                String binary = p[4];

                // Only show rows belonging to the current logged-in user
                if (fileUserId.equals(userId)) {
                    String readableTime = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss")
                            .format(new java.util.Date(timeMillis));

                    model.addRow(new Object[]{
                            recordId,
                            action,
                            readableTime,
                            binary
                    });
                }
            }
        }

        tableUserLogingHistory.setModel(model);

    } catch (Exception ex) {
        ex.printStackTrace();
    }
}

